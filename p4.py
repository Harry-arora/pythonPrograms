# List slicing in Python

my_list = ['h','a','r','r','y']

# elements from index 1 to index 3
print(my_list[1:4])

# elements from index 2 to end
print(my_list[2:])

# elements start to end
print(my_list[:])

# Appending and Extending lists in Python
odd = [1, 3, 5]

odd.append(7)

print(odd)

odd.extend([9, 11, 13])

print(odd)

# Concatenating and repeating lists
odd = [5, 3, 1]

print(odd + [9, 4, 5])

