# data types in python
a = 5   #integer
b = 2.9  #   float
c = 1+5j  #   complex
print(a, type(a))
print(b, type(b))
print(c, type(c))

a = [1,2,6,9,0,7,8] #this is list
a[1:4]
print(a[1:4])  #slicing in list

t = (5,'raju',877) # this is tuple
print(type(t))

s = 'hello guys how are you'
print(type(s))   # this is string
# strings are immutable in python
s[6]
print(s[6])

set = {2,3,4,5,6,7,90}
print(type(set))

d = {1:6, 2:98, 3:78.09, 9080:3} # {value:key}

print(type(d))
print(d)

